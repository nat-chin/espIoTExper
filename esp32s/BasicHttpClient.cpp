void setup() {
    WRITE_PERI_REG(RTC_CNTL_BROWN_OUT_REG, 0);
    USE_SERIAL.begin(115200);

    USE_SERIAL.println();
    USE_SERIAL.println();
    USE_SERIAL.println();

    for(uint8_t t = 4; t > 0; t--) {
        USE_SERIAL.printf("[SETUP] WAIT %d...\n", t);
        USE_SERIAL.flush();
        delay(1000);
    }

    // Begin ESP32 in STA mode , and Add Access point nearby (can add multiple)
    wifiMulti.addAP(ssid, password);
    // wifiMulti.addAP("ssid", "password");
    // wifiMulti.addAP("ssid", "password");

    // ESP32 -> HTTP Client
    // SomeWebpage -> Server
}

void loop() {
    // Check for WiFi connection for each runtime
    if((wifiMulti.run() == WL_CONNECTED)) {
        // init HTTPclient -> 
        HTTPClient http;

        USE_SERIAL.print("[HTTP] begin...\n");
        // configure traged server and url
        //http.begin("https://www.howsmyssl.com/a/check", ca); //HTTPS

        // Begin HTTP with target webpage URL
        http.begin("http://example.com/index.html"); //HTTP

        USE_SERIAL.print("[HTTP] GET...\n");

        // start connection and send HTTP header to webpage
        int httpCode = http.GET();

        // httpCode will be negative on error
        if(httpCode > 0) {
            // HTTP header has been send and Server response header has been handled
            USE_SERIAL.printf("[HTTP] GET... code: %d\n", httpCode);

            // file found at server (which mean http://example.com/index.html Exist)
            if(httpCode == HTTP_CODE_OK) {
                // Payload response that server send back
                String payload = http.getString();
                USE_SERIAL.println(payload);
            }
        } else {
            USE_SERIAL.printf("[HTTP] GET... failed, error: %s\n", http.errorToString(httpCode).c_str());
        }

        http.end();
    }

    delay(1000); // Polling rate of 1 sec
}
